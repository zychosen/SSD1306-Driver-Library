#include "i2c.h"
#include "ssd1306.h"
#include "font5x8.h"
/**
 * main.c
 */
const uint8_t bitmap [] = {  /* Horizontal read - 1 byte */
 	0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, 0xe0, 0x00, 0x01, 0xe0, 0x00, 0x01, 0xe0, 0x00, 0x03,
	0xf0, 0x00, 0x1f, 0xfe, 0x00, 0xff, 0xff, 0xc0, 0xff, 0xff, 0xc0, 0xff, 0xff, 0xc0, 0xff, 0xff,
	0xc0, 0xff, 0xff, 0xc0, 0xff, 0xff, 0xc0, 0xff, 0xff, 0xc0, 0xff, 0xff, 0xc0
};
int main(void)
{
	WDTCTL = WDTPW | WDTHOLD;	        /* stop watchdog timer */
	static const uint8_t address = 0x3C;    /* I2C address of slave */
	i2c_setup(address);
	ssd1306_setup();
	int i;
	while (1) {
		drawBitmap(bitmap, 18, 15, 20, 40);
		update_screen();
		for (i = 30000; i > 0; i--);
	}
}